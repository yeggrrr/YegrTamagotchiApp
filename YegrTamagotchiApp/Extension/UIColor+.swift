import UIKit

extension UIColor {
    static func primaryBackgroundColor() -> UIColor {
        return UIColor(red: 245/255, green: 252/255, blue: 252/255, alpha: 1)
    }
    
    static func fontBorderColor() -> UIColor {
        return UIColor(red: 77/255, green: 106/255, blue: 120/255, alpha: 1)
    }
}
